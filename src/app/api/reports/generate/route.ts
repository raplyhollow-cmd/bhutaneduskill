import { NextRequest, NextResponse } from "next/server";
import { auth } from "@clerk/nextjs/server";
import { db } from "@/lib/db";
import { users } from "@/lib/db/schema";
import { eq, and, desc } from "drizzle-orm";
import { nanoid } from "nanoid";

// ============================================================================
// POST /api/reports/generate - Generate government report
// ============================================================================

export async function POST(request: NextRequest) {
  try {
    const { userId } = await auth();
    if (!userId) {
      return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
    }

    const currentUser = await db.query.users.findFirst({
      where: eq(users.clerkUserId, userId),
    });

    if (!currentUser) {
      return NextResponse.json({ error: "User not found" }, { status: 404 });
    }

    const body = await request.json();
    const {
      templateId,
      reportPeriod,
      reportingPeriodStart,
      reportingPeriodEnd,
      dueDate,
      autoGenerateData,
    } = body;

    if (!templateId || !reportPeriod) {
      return NextResponse.json(
        { error: "Missing required fields: templateId, reportPeriod" },
        { status: 400 }
      );
    }

    const {
      reportTemplates: reportTemplatesTable,
      generatedReports: generatedReportsTable,
    } = await import("@/lib/db/reports-schema");

    // Get template
    const template = await db.query.reportTemplates.findFirst({
      where: eq(reportTemplatesTable.id, templateId),
    });

    if (!template) {
      return NextResponse.json({ error: "Template not found" }, { status: 404 });
    }

    // Generate report data based on template
    let reportData = {};
    let summary = "";

    if (autoGenerateData) {
      // Auto-generate data based on report type
      const generated = await generateReportData(
        template.code,
        currentUser.schoolId || "",
        reportPeriod,
        reportingPeriodStart,
        reportingPeriodEnd
      );
      reportData = generated.data;
      summary = generated.summary;
    }

    // Create generated report
    const [report] = await db.insert(generatedReportsTable).values({
      id: nanoid(),
      schoolId: currentUser.schoolId || "",
      templateId,
      reportName: `${template.name} - ${reportPeriod}`,
      reportCode: `${template.code}-${reportPeriod}`,
      reportPeriod,
      reportingPeriodStart,
      reportingPeriodEnd,
      generatedDate: new Date().toISOString(),
      dueDate,
      status: "draft",
      reportData,
      summary,
      preparedBy: currentUser.id,
      preparedDate: new Date().toISOString(),
      agency: template.agency,
      reportType: template.reportType,
      frequency: template.frequency,
      createdAt: new Date(),
      updatedAt: new Date(),
    }).returning();

    // Create specific report type data
    await createSpecificReportData(report.id, template.code, currentUser.schoolId || "", reportData);

    return NextResponse.json({ report }, { status: 201 });
  } catch (error) {
    console.error("Report generation error:", error);
    return NextResponse.json({ error: "Failed to generate report" }, { status: 500 });
  }
}

// ============================================================================
// Helper function to generate report data based on type
// ============================================================================

async function generateReportData(
  templateCode: string,
  schoolId: string,
  reportPeriod: string,
  periodStart?: string,
  periodEnd?: string
): Promise<{ data: Record<string, any>; summary: string }> {
  const data: Record<string, any> = {
    schoolId,
    reportPeriod,
    generatedAt: new Date().toISOString(),
  };

  let summary = "";

  switch (templateCode) {
    case "STU_ATTEND": {
      // Student attendance report
      const { students: studentsTable, attendance: attendanceTable } = await import("@/lib/db/schema");

      const students = await db.query.students.findMany({
        where: eq(studentsTable.schoolId, schoolId),
      });

      data.totalStudents = students.length;
      data.reportPeriod = reportPeriod;
      summary = `${students.length} students enrolled for ${reportPeriod}`;

      // Calculate attendance rates
      // In production, would query attendance table
      data.overallAttendanceRate = 85; // Placeholder
      break;
    }

    case "STU_PERF": {
      // Student performance report
      data.totalStudents = 0;
      data.overallPassPercentage = 0;
      summary = "Student performance report";
      break;
    }

    case "INFRA": {
      // Infrastructure report
      data.totalClassrooms = 20;
      data.hasLibrary = true;
      data.hasComputerLab = true;
      data.computerCount = 30;
      summary = "Infrastructure report";
      break;
    }

    case "STAFF": {
      // Staff report
      const { teachers: teachersTable } = await import("@/lib/db/schema");

      const teachers = await db.query.teachers.findMany({
        where: eq(teachersTable.schoolId, schoolId),
      });

      data.totalTeachers = teachers.length;
      data.totalNonTeachingStaff = 10;
      data.totalStaff = teachers.length + 10;
      summary = `${teachers.length} teachers, ${data.totalStaff} total staff`;
      break;
    }

    case "FINANCE": {
      // Financial report
      data.governmentGrant = 500000;
      data.schoolFees = 100000;
      data.totalIncome = 600000;
      data.salaries = 400000;
      data.totalExpenditure = 550000;
      summary = "Financial report";
      break;
    }

    case "DEMOG": {
      // Demographic report
      data.totalEnrollment = 500;
      data.newEnrollment = 50;
      data.genderBreakdown = { male: 260, female: 240 };
      summary = "Demographic report";
      break;
    }

    default:
      summary = `${templateCode} report for ${reportPeriod}`;
  }

  return { data, summary };
}


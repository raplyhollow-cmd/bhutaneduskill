// ============================================================================
// GET /api/reports/generate - Get available report templates
// ============================================================================

export async function GET(request: NextRequest) {
  try {
    const { userId } = await auth();
    if (!userId) {
      return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
    }

    const currentUser = await db.query.users.findFirst({
      where: eq(users.clerkUserId, userId),
    });

    if (!currentUser) {
      return NextResponse.json({ error: "User not found" }, { status: 404 });
    }

    const { reportTemplates: reportTemplatesTable } = await import("@/lib/db/reports-schema");

    const templates = await db.query.reportTemplates.findMany({
      where: eq(reportTemplatesTable.isActive, true),
      orderBy: [reportTemplatesTable.name],
    });

    // Group by agency
    const grouped = templates.reduce((acc, template) => {
      if (!acc[template.agency]) {
        acc[template.agency] = [];
      }
      acc[template.agency].push(template);
      return acc;
    }, {} as Record<string, typeof templates>);

    return NextResponse.json({
      templates: grouped,
      allTemplates: templates,
    });
  } catch (error) {
    console.error("Report templates fetch error:", error);
    return NextResponse.json({ error: "Failed to fetch templates" }, { status: 500 });
  }
}

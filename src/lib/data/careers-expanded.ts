/**
 * Helper function to search careers by keyword
 */
export function searchCareers(keyword: string): ExpandedCareer[] {
  const lowerKeyword = keyword.toLowerCase();
  return expandedCareersData.filter((career) =>
    career.titleLower.includes(lowerKeyword) ||
    career.description.toLowerCase().includes(lowerKeyword) ||
    career.industry.toLowerCase().includes(lowerKeyword) ||
    career.category.toLowerCase().includes(lowerKeyword) ||
    career.skills.some((skill) => skill.toLowerCase().includes(lowerKeyword))
  );
}

/**
 * Helper function to get careers by RIASEC code
 */
export function getCareersByRIASEC(riasecCode: string): ExpandedCareer[] {
  const primaryCode = riasecCode[0].toUpperCase();
  return expandedCareersData.filter((career) =>
    career.riasecCode === primaryCode ||
    career.hollandCodes.some((code) => code === primaryCode)
  );
}

/**
 * Helper function to get careers by education level
 */
export function getCareersByEducation(education: "Class10" | "Class12" | "Certificate" | "Diploma" | "Bachelor" | "Master"): ExpandedCareer[] {
  return expandedCareersData.filter((career) => {
    if (education === "Master") return career.educationLevel === "Master";
    if (education === "Bachelor") return ["Bachelor", "Master"].includes(career.educationLevel);
    if (education === "Diploma") return ["Diploma", "Certificate"].includes(career.educationLevel);
    if (education === "Class12") return ["Diploma", "Certificate", "Bachelor", "Master"].includes(career.educationLevel);
    if (education === "Class10") return true;
    return false;
  });
}

/**
 * Helper function to get Bhutan-specific careers
 */
export function getBhutanCareers(): ExpandedCareer[] {
  return expandedCareersData.filter((career) => career.bhutanSpecific);
}

/**
 * Helper function to get careers by demand level
 */
export function getCareersByDemand(demand: "high" | "medium" | "low"): ExpandedCareer[] {
  return expandedCareersData.filter((career) => career.bhutanDemand === demand);
}

/**
 * Helper function to get careers by outlook
 */
export function getCareersByOutlook(outlook: "Growing" | "Stable" | "Declining" | "Emerging"): ExpandedCareer[] {
  return expandedCareersData.filter((career) => career.bhutanOutlook === outlook);
}

/**
 * Helper function to get careers by category
 */
export function getCareersByCategory(category: string): ExpandedCareer[] {
  return expandedCareersData.filter((career) =>
    career.category.toLowerCase() === category.toLowerCase()
  );
}
